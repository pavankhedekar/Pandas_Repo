import numpy as np

# Create two arrays
a = np.array([10, 20, 30, 40])
b = np.array([1, 2, 3, 4])

print("Array a:", a)
print("Array b:", b)

# Addition
print("\nAddition:", a + b)

# Subtraction
print("Subtraction:", a - b)

# Multiplication
print("Multiplication:", a * b)

# Division
print("Division:", a / b)

# Element-wise power (**)
print("Power (a ** b):", a ** b)

# Modulo operation (%)
print("Modulo (a % b):", a % b)
